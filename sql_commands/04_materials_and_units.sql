CREATE TABLE `units` (
 `id` int(11) NOT NULL AUTO_INCREMENT,
 `unit` varchar(50) not null,
 `description` varchar(300) not null,
 PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `materials` (
 `id` int(11) NOT NULL AUTO_INCREMENT,
 `material` varchar(150) not null,
 `description` varchar(300) not null default "",
 `unit`  int(11) NOT NULL,
 `amount`  double(10, 2) DEFAULT 0, 

 PRIMARY KEY (`id`),
 KEY `unit` (`unit`),
 CONSTRAINT `material_unit` FOREIGN KEY (`unit`) REFERENCES `units` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION 
 
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `materials_arrival` (
 `id` int(11) NOT NULL AUTO_INCREMENT,
 `date` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
 `material` int(11) NOT NULL,
 `amount`  double(10, 2) DEFAULT 0, 

 PRIMARY KEY (`id`),
 KEY `material` (`material`),
 CONSTRAINT `material_arrival` FOREIGN KEY (`material`) REFERENCES `materials` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION 
 
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `materials_consumption` (
 `id` int(11) NOT NULL AUTO_INCREMENT,
 `date` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
 `material` int(11) NOT NULL,
 `amount`  double(10, 2) DEFAULT 0, 

 PRIMARY KEY (`id`),
 KEY `material` (`material`),
 CONSTRAINT `material_aconsumption` FOREIGN KEY (`material`) REFERENCES `materials` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION 
 
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

